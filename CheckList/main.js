let realizado1 = document.getElementById('tarea1');
let tachado1 = document.querySelector('.tachado1');

realizado1.addEventListener('change', e=>{
    if(e.target.checked === true){
        tachado1.className = 'tachar';
        alert('La tarea 1 fue realizada');
    }else{
        tachado1.className = 'tachado';
    }
})

